class oops: # Object
    # the __init__ is the constructor
    def __init__(self, a, b):
        self.a = a
        self.b = b
    def add(self):
        print(self.a + self.b)

o1 = oops(10,50)
o1.add()
o2 = oops(50,50)
o2.add()
